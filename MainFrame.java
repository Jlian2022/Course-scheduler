    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
         
        
        StudentEntry student = (StudentEntry)StudentnameandIDjComboBox1.getSelectedItem();
        String selected_student = student.getStudentID();

        ArrayList<String> semester = SemesterQueries.getSemesterList();

        for (String current_semester : semester) {
   
            jTextArea2.append("For semester "+ current_semester+"\n");
            for (ScheduleEntry current_schedule : ScheduleQueries.getScheduleByStudent(current_semester, selected_student)) {
                if(current_schedule.getStatus().equals("w")) {
                    ScheduleQueries.dropStudentScheduleByCourse(currentSemester, selected_student, current_schedule.toString());
                    
                
                
                
                }
                else {
                    //jTextArea2.append("For semester"+ current_semester+"\n");
                    jTextArea2.append(selected_student.toString()+" Dropped from course " + current_schedule.getCourseCode() + "\n");
                    ScheduleQueries.dropStudentScheduleByCourse(currentSemester, selected_student, current_schedule.getCourseCode());
                    
                    ArrayList<ScheduleEntry> waitlisted_student = ScheduleQueries.getWaitlistedStudentsByClass(currentSemester, current_schedule.getCourseCode());
                    
                    if(waitlisted_student.size()==0){
                        //do nothing
                    
                    
                    
                    
                    }
                
                    else{
                        ScheduleQueries.updateScheduleEntry(waitlisted_student.get(0));
                        jTextArea2.append(waitlisted_student.get(0).getStudentID()+" has been scheduled into "+ current_schedule.getCourseCode() +"\n");
                    
                    
                    
                    }
                }

          
            }
        }

        StudentQueries.dropStudent(selected_student);
        rebuidstudentcombobox();
        

    }//GEN-LAST:event_dropStudentButtonActionPerformed

