    private void ScheduleClassSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ScheduleClassSubmitButtonActionPerformed

        Object semester = currentSemesterComboBox.getSelectedItem();
        String selectedSemester = semester.toString();

        Object classCode = classComboBox.getSelectedItem();
        String selecedclasscode = classCode.toString();

        StudentEntry student = (StudentEntry) scheduleclassstudentcombobox.getSelectedItem();
        String selectedStudent = student.getStudentID();

        Timestamp timestamp = new Timestamp(System.currentTimeMillis());

        String status = "";

        if ((ClassQueries.getClassSeats(selectedSemester, selecedclasscode)) > (ScheduleQueries.getScheduledStudentCount(selectedSemester, selecedclasscode))) {
            status = "s";
            scheduleclassLabel.setText(selectedStudent + " has been scheduled in the class");

        } else {
            status = "w";
            scheduleclassLabel.setText(selectedStudent + " has been waitlisted for the class");
        }

        ScheduleQueries.addScheduleEntry(new ScheduleEntry(selectedSemester, selecedclasscode, selectedStudent, status, timestamp));
//        rebuildclassComboBox();


    }//GEN-LAST:event_ScheduleClassSubmitButtonActionPerformed

